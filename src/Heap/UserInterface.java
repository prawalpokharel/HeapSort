package Heap;

public class UserInterface {
        //A utility function to print array of size n
        static void printArray(Integer[] arr) {
            Integer n = arr.length;
            for(int i = 0; i<n; ++i) {
                System.out.print(arr[i] + " ");
                System.out.println();
            }
}}
